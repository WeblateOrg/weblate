from __future__ import annotations

from pathlib import Path
from textwrap import wrap
from typing import TYPE_CHECKING

from weblate.addons.events import POST_CONFIGURE_EVENTS, AddonEvent
from weblate.addons.models import ADDONS, Addon
from weblate.trans.models import Component, Project
from weblate.utils.management.base import BaseCommand, CommandError
from weblate.utils.rst import format_rst_string, format_table

if TYPE_CHECKING:
    from collections.abc import Iterable

SKIP_FIELDS: tuple[tuple[str, str]] = (
    ("weblate.flags.bulk", "path"),  # Used internally only
)


def event_link(event: AddonEvent) -> str:
    return f"addon-event-{event.label.lower().replace(' ', '-')}"


def sorted_events(events: Iterable[AddonEvent]) -> Iterable[AddonEvent]:
    return sorted(events, key=lambda event: event.label)


SHARED_PARAMS = {"engines", "file_format", "events"}


class Command(BaseCommand):
    help = "List installed add-ons"

    def add_arguments(self, parser):
        parser.add_argument(
            "-o",
            "--output",
            dest="output",
            type=Path,
            default=None,
            help="Optional output file path for the generated add-ons list",
        )

    def insert_autogenerated_section(
        self,
        filepath: Path,
        autogenerated_content: str,
        start_marker: str,
        end_marker: str,
    ):
        with filepath.open("r", encoding="utf-8") as f:
            content = f.read()
        try:
            start = content.index(start_marker) + len(start_marker)
            end = content.index(end_marker)
        except ValueError as err:
            msg = f"Missing markers '{start_marker}' or '{end_marker}' in {filepath}"
            raise CommandError(msg) from err

        new_content = content[:start] + autogenerated_content + content[end:]
        with filepath.open("w", encoding="utf-8") as f:
            f.write(new_content)

    def write_lines(
        self,
        output_file: Path | None,
        lines: list[str],
        autogenerated_start_marker: str,
        autogenerated_end_marker: str,
    ):
        if output_file:
            self.insert_autogenerated_section(
                output_file,
                "".join(lines),
                autogenerated_start_marker,
                autogenerated_end_marker,
            )
        else:
            self.stdout.write(
                ".. This section is automatically generated by `./manage.py list_addons`. Do not edit manually."
            )
            self.stdout.write(autogenerated_start_marker)
            for line in lines:
                self.stdout.write(line)
            self.stdout.write(autogenerated_end_marker)

    def get_help_text(self, field, name: str) -> str:
        result = []
        if field.help_text:
            result.append(format_rst_string(field.help_text))
        choices = getattr(field, "choices", None)
        if choices:
            if name in SHARED_PARAMS:
                # Add link to shared docs
                result.append(f":ref:`addon-choice-{name}`")
            elif name not in {
                "component",
                "source",
                "target",
            }:
                # List actual choices
                if result:
                    result.append("")
                result.extend(self.get_choices_table(choices))
        return "\n".join(result)

    def get_choices_table(self, choices: list[tuple[str, str]]) -> list[str]:
        result = [
            ".. list-table:: Available choices:",
            "   :width: 100%",
            "",
        ]
        for value, description in choices:
            if not value and not description:
                continue
            result.extend(
                (
                    f"   * - ``{value}``".replace("\\", "\\\\"),
                    f"     - {format_rst_string(description)}".replace("\\", "\\\\"),
                )
            )
        return result

    def handle(self, *args, **options) -> None:
        """List installed add-ons."""
        output_file = options.get("output")
        self.write_lines(
            output_file,
            self.get_events_lines(),
            ".. AUTOGENERATED START: events",
            ".. AUTOGENERATED END: events",
        )
        # Shared parameters
        self.params = SHARED_PARAMS.copy()
        self.param_docs: dict[str, list[str]] = {}

        self.write_addons_lines(output_file)

    def get_events_lines(self) -> list[str]:
        result = []
        result.append("""

Events that trigger add-ons
+++++++++++++++++++++++++++

""")
        event_descriptions = AddonEvent.descriptions()
        for event in sorted_events(AddonEvent):
            result.extend(
                (
                    f".. _{event_link(event)}:\n\n",
                    f"{event.label}\n",
                    "-" * len(event.label) + "\n",
                    "\n",
                )
            )
            if description := event_descriptions.get(event):
                result.extend((description, "\n"))
            result.append("\n")
        return result

    def write_addons_lines(self, output_file: Path | None) -> list[str]:
        if not output_file:
            self.stdout.write("""
Built-in add-ons
++++++++++++++++
\n\n""")
        # Actual add-ons
        fake_addon = Addon(component=Component(project=Project(pk=-1), pk=-1))
        for addon_name, obj in sorted(ADDONS.items()):
            result = []
            result.extend(
                (
                    "\n",
                    f".. _addon-{obj.name}:",
                    "\n\n",
                    str(obj.verbose) + "\n",
                    "-" * len(obj.verbose),
                    "\n",
                )
            )
            if obj.doc_versions:
                for version in obj.doc_versions:
                    result.extend(("\n", str(version), "\n"))
            result.extend(("\n", f":Add-on ID: ``{obj.name}``", "\n"))
            prefix = ":Configuration: "
            if obj.settings_form:
                form = obj(fake_addon).get_settings_form(None)
                table: list[list[str | list[list[str]]]] = [
                    [
                        f"``{name}``",
                        str(field.label),
                        self.get_help_text(field, name),
                    ]
                    for name, field in form.fields.items()
                    if (addon_name, name) not in SKIP_FIELDS
                ]

                for table_row in format_table(table, None):
                    result.append(f"{prefix}{table_row}")
                    if not prefix.isspace():
                        prefix = " " * len(prefix)

                for name in self.params & set(form.fields):
                    field = form.fields[name]
                    self.params.remove(name)
                    self.param_docs[name] = [
                        f".. _addon-choice-{name}:",
                        "",
                        f"{field.label}",
                        "-" * len(field.label),
                        "",
                        *self.get_choices_table(field.choices),
                    ]
            else:
                result.append(f"{prefix}`This add-on has no configuration.`")
            events = ", ".join(
                f":ref:`{event_link(event)}`" for event in sorted_events(obj.events)
            )
            if POST_CONFIGURE_EVENTS & set(obj.events):
                events = f":ref:`addon-event-add-on-installation`, {events}"
            result.extend(
                [
                    "\n",
                    f":Triggers: {events}",
                    "\n\n",
                    "\n".join(wrap(str(obj.description), 79)),
                    "\n",
                ]
            )

            self.write_lines(
                output_file,
                result,
                f".. AUTOGENERATED START: {addon_name}",
                f".. AUTOGENERATED END: {addon_name}",
            )
